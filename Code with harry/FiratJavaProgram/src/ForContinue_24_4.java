public class ForContinue_24_4 {
    public static void main(String[] args) {

        for (int i = 0; i < 5; i++) {
            if (i == 1) {
                System.out.println("Ending the loop");
            }
            System.out.println(i);
            System.out.println("Java is great");
        }
    }
}
